package gg.sora.otherDTO;

public class challlist {
private String s_name;
private String s_aid;
public challlist() {
}
public challlist(String s_name, String s_aid) {
	super();
	this.s_name = s_name;
	this.s_aid = s_aid;
	}

	public String getS_name() {
		return s_name;
	}

	public void setS_name(String s_name) {
		this.s_name = s_name;
	}

	public String getS_aid() {
		return s_aid;
	}

	public void setS_aid(String s_aid) {
		this.s_aid = s_aid;
	}


}
